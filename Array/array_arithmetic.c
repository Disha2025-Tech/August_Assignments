//Arithmetic Operations using array

#include <stdio.h>
int main()
{
    int arr[]={2, 4, 189, 490, 823};

    printf("Addition of arr[1] and arr[2]=%d\n",arr[1]+arr[2]);
    printf("Multiplication of arr[0] and arr[1]=%d\n",arr[0]*arr[1]);
    printf("Subtraction of arr[4] and arr[3]=%d\n",arr[4]-arr[3]);
    printf("Division of arr[1] and arr[0]=%d\n",arr[1]/arr[0]);

    return 0;
}

//OUTPUT
/*

Addition of arr[1] and arr[2]=193
Multiplication of arr[0] and arr[1]=8
Subtraction of arr[4] and arr[3]=333
Division of arr[1] and arr[0]=2


*/